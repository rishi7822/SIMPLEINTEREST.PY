#python program to find simple interest
def simple_interest(p,t,r):
    print("the principal is",p)
    print("the time period is",t)
    print("the rate is",r)

    si = (p*t*r)/100

    print("the simple interest is",si)
    return si

simple_interest(35,40,50)
